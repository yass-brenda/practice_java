package com.company.paquete;


public class Pirnt_Methods {
    public static void main(String args[]) {
        // Print after of last print => UlissesBarrios
        System.out.print("Ulisses");
        System.out.print("Barrios");

        // Print in new line =>

        /* Ulisses
        Barios
         */
        System.out.println("Ulisses");
        System.out.println("Barrios");

        // Print new line: in new line =>
        /* Ulisses
           Brrios
        * */
        System.out.println( "Ulisses\nBarrios" );

        // Print horizontal tab: with space of tabulations => Bienvenido      a       la programacion
        System.out.println( "Bienvenido\ta\tla programacion");

        // Print car return: only print from the last car return => la programacion
        System.out.println( "Bienvenido\ra\rla programacion");

        // Print a Backslash(\) => Bienvenido\a\la programacion
        System.out.println( "Bienvenido\\a\\la programacion");


        // Print double quote => "Entre comillas"
        System.out.println( "\"Entre comillas\"");


    }
}
