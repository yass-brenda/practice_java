package com.company.cycles;

public class For {
    public static void main(String args[]){
        for(int i = 0;i < 10; i++ ){
            System.out.println("Ulisses");
        }
        for(int i = 10;i > 0 ; i-- ){
            System.out.println("Ulisses");
        }
        for(int i = 0;i < 20 ; i+=2 ){
            System.out.println("Ulisses");
        }
    }
}

